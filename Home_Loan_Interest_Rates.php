	<h1>Home Loan Interest Rates</h1>
<p1>The interest rate in general on home loans is also dependent on interest rate cycle as well. Home Loan interest rates in particular are dependent on the home loan amount, loan tenure and the profile of the borrower. The borrower’s credit history and score under credit information bureau like CIBIL also has an impact on the home loan interest rate. The rate of interest on home loan can vary from lender to lender. Lenders like SBI offer different interest rate for its new customers against its existing customers.<br>
	Home Loan interest rates can primarily be classified into two categories i.e. fixed rate and floating rate of interest. There are very few lenders in India who offer pure fixed home loan rates where the rate of interest remains constant for the entire tenure of the home loan, while most lenders have a reset clause of 3-5 years. In floating home type, the rate of interest on such loans is subject to change whenever there are changes in the repo rates announced by RBI or any change is there in base rate of the bank. Borrower should opt for fixed rate of interest only if he is certain that the rate of interest is the lowest in the interest cycle. HDFC home loan interest rate is fixed for the entire tenure of loan, whereas SBI home loan interest rate is floating.<br>

Some lenders even offer hybrid home loan interest rates. In recent times, some lenders have come up with innovative home loan products like teaser / dual rate of interest where the interest rate on such loans remains fixed for initial 1-5 years and thereafter it automatically moves to a normal floating rate of interest.<br>

In case when the borrower prepays the housing loan taken under floating rate of interest, the lender cannot levy any penalty for prepayment of such loan as per the guidelines issued by RBI and National Housing Bank (NHB).<br>

The borrower can exercise an option of converting from fixed interest rate to floating interest rate and vice versa by without paying any fee or penalty.<br>

Normally, the home loan interest rates for banks are expressed as certain point above Base Rate and certain point above or below PLR (Prime Lending Rate) popularly known as spread.<br>

The banks follow base rate system for calculating their interest rate, whereas the housing finance companies like HDFC Limited, LIC housing finance, etc. follow PLR system<br>

The bank cannot lend below its base rate to any borrower. Any reduction in Base Rates automatically benefits all existing borrowers of the bank in their home loan interest rate. No such compulsions exist for the HFC’s and hence banks that follow the Base rate systems are likely to be forced to pass on the benefits of drop in  home loan interest rates to its existing customers whereas no such system exists for Housing Finance Companies.<br>

Hence, when there is a downward revision in the interest rates, the benefit, which has a certain degree of ambiguity in the PLR system, happens with a greater degree of transparency in the Base Rate system.<p1><br><br>

	<h1>Guide to Home Loan Interest Rates:</h1>
	<p1>Interest rate on home loan is something that one has to pay in lieu of the loan provided by the bank/financial institution. Home loan Interest rate is very important element when it comes to choosing a home loan for a customer & it also helps the customer on taking right decision or on pinning down that through which bank or financial institution he or she can go for. Depending on this interest rate, the loan amount and the tenure of the loan, your EMI is calculated which is how you repay your loan to the bank. In case the interest rate is higher, the EMI would be higher and in case the interest rate is lower, the EMI would be lower. Thus while choosing a bank with lower home loan interest rates; you can certainly increase your monthly savings. But please keep in mind that interest rate is not the only criteria to choose a loan. There are various other parameters as well. The EMI is calculated on a monthly reducing balance method.<br>

Your home loan eligibility or how much loan you are eligible for is also calculated on the basis of the interest rates. In case you opt for a bank with a lower interest rate, then your eligibility will be higher compared to a bank that is offering a higher interest rate.
